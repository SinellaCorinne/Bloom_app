// lib/helpers/db_helper.dart
import 'package:sqflite/sqflite.dart' as sql;
import 'package:path/path.dart' as path;

class DBHelper {
  static Future<sql.Database> database() async {
    final dbPath = await sql.getDatabasesPath();
    return sql.openDatabase(
      path.join(dbPath, 'bloom.db'),
      onCreate: (db, version) {
        // ⚠️ CRUCIAL : Vérifie que toutes les colonnes sont là !
        return db.execute(
            'CREATE TABLE tasks(id TEXT PRIMARY KEY, title TEXT, description TEXT, priority TEXT, date TEXT, isSynced INTEGER)'
        );
      },
      version: 1,
    );
  }

  static Future<void> insert(String table, Map<String, Object> data) async {
    final db = await DBHelper.database();
    await db.insert(table, data, conflictAlgorithm: sql.ConflictAlgorithm.replace);
  }

  static Future<List<Map<String, dynamic>>> getData(String table) async {
    final db = await DBHelper.database();
    return db.query(table);
  }
}